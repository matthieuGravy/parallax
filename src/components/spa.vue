<template>
  <h3>Avantages d'une SPA ?</h3>
  <ul>
    <li>
      <h4>Expérience utilisateur fluide :</h4>
      <p>
        Grâce à une SPA, une fois que la page initiale est chargée, les interactions avec le site se
        font de manière fluide et instantanée. Les utilisateurs peuvent naviguer entre les
        différentes sections du site sans avoir à attendre le chargement d'une nouvelle page à
        chaque fois. Cela offre une expérience plus rapide et agréable pour les utilisateurs.
      </p>
    </li>
    <li>
      <h4>Interaction dynamique :</h4>
      <p>
        Les SPAs permettent d'interagir avec les utilisateurs de manière dynamique, sans recharger
        toute la page. Vous pouvez mettre à jour dynamiquement le contenu, afficher des animations,
        charger des données en arrière-plan, etc. Cela permet de créer des interfaces utilisateur
        réactives et interactives.
      </p>
    </li>
    <li>
      <h4>Chargement initial plus rapide :</h4>
      <p>
        Dans une SPA, seule la page initiale est chargée lors de la première visite. Les ressources
        supplémentaires telles que les scripts, les styles et les données sont chargées au fur et à
        mesure de la navigation. Cela réduit le temps de chargement initial, ce qui est crucial pour
        offrir une bonne expérience utilisateur, en particulier sur les appareils mobiles ou les
        connexions Internet plus lentes.
      </p>
    </li>
    <li>
      <h4>Architecture modulaire :</h4>
      <p>
        Les SPAs reposent généralement sur une architecture modulaire, facilitant ainsi la gestion
        et la réutilisation du code. Vous pouvez découper votre application en composants
        réutilisables, améliorant ainsi la maintenabilité et la scalabilité de votre code.
      </p>
    </li>
    <li>
      <h4>SEO amélioré :</h4>
      <p>
        Historiquement, les SPAs posaient des problèmes de référencement (SEO) car le contenu était
        généralement généré dynamiquement côté client. Cependant, grâce aux avancées récentes, il
        est possible de mettre en place des techniques de pré-rendu ou de rendu côté serveur
        (Server-Side Rendering - SSR) pour rendre le contenu des SPAs accessible aux moteurs de
        recherche, améliorant ainsi leur référencement.
      </p>
    </li>
  </ul>
